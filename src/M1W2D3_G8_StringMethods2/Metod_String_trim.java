public class Metod_String_trim {
    public static void main(String[] args) {
        // trim= stringin başında ve sonundaki boşluk karakterlerini temizler...

        String Str="  Ben Evdeyim  ";

        System.out.println("Orjinal Hali = ->" + Str+"<-");
        System.out.println("Baş ve Sonu Boşlukları Temizlenmiş Hali = ->" + Str.trim()+"<-");
        System.out.println("BOŞLUKLARI temizle= "+Str.trim());




    }



}
