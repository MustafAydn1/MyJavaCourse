package M1W2D4_G9_JavaOperatorleri;

public class AritmetikOperators_Bölme {
    public static void main(String[] args) {


        int a=10;
        int b= 3;

        System.out.println("a/b= "+(a/b)); //int sonucu daima tam kısmını verir

        System.out.println("a/b= "+ ((double)a/b)); // sayılardan herhangi birini double  yap ;ondalıklı istıyorsak

        System.out.println("a/b= "+ ((float)a/b));  //float ta 7 hane --- double de 15 hane...

        System.out.println("a/b= "+ (double)(a/b));  // sonuc; bölmeyi int yapar. sonra double olarak ekler virgülü.



        double c=3.14;
        double d=2.1;

        System.out.println("c/d= "+(c/d));
        System.out.println("c/d= "+((float)c/d));

















    }







}

