package M1W4D2_G17_NestedLoop;

public class NestedLoop4 {
    public static void main(String[] args) {

        for (int satır =1; satır<=5 ;satır++) {

            for(int sutun=1; sutun<=satır; sutun++){ //sutun sayısı satır sayısı kadar oldugu için.
                System.out.print("*");               // yan yana gitmesi için ln olmaz.
            }
            System.out.println();
        }
// sorularda biri ile digeri arasındaki ilişkiyi farketmeye bakkk.


    }
}
